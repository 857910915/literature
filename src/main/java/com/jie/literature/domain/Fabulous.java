package com.jie.literature.domain;

import java.util.Date;

public class Fabulous {
    private Integer fabId;

    private Integer fabAid;

    private Integer fabUid;

    private Date fabCreatetime;

    private Integer fabStatus;

    public Integer getFabId() {
        return fabId;
    }

    public void setFabId(Integer fabId) {
        this.fabId = fabId;
    }

    public Integer getFabAid() {
        return fabAid;
    }

    public void setFabAid(Integer fabAid) {
        this.fabAid = fabAid;
    }

    public Integer getFabUid() {
        return fabUid;
    }

    public void setFabUid(Integer fabUid) {
        this.fabUid = fabUid;
    }

    public Date getFabCreatetime() {
        return fabCreatetime;
    }

    public void setFabCreatetime(Date fabCreatetime) {
        this.fabCreatetime = fabCreatetime;
    }

    public Integer getFabStatus() {
        return fabStatus;
    }

    public void setFabStatus(Integer fabStatus) {
        this.fabStatus = fabStatus;
    }
}